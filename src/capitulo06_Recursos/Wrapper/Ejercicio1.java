package capitulo06_Recursos.Wrapper;

public class Ejercicio1 {

	public static void main(String[] args) {
		

		// Wrapper para el tipo de datos "byte"
			System.out.println("\nValor maximo de un byte: " + Byte.MAX_VALUE);
			System.out.println("Valor minimo de un byte: " + Byte.MIN_VALUE);
			System.out.println("Tamaño del byte: " + Byte.SIZE);
		
		// Wrapper para el tipo de datos "Int"
			System.out.println("\nValor maximo de Int: " + Integer.MAX_VALUE);
			System.out.println("Valor minimo de Int: " + Integer.MIN_VALUE);
			System.out.println("Tamaño del int: " + Integer.SIZE);
		
		
		// Wrapper para el tipo de datos "long"
			System.out.println("\nValor maximo de un Long: " + Long.MAX_VALUE);
			System.out.println("Valor minimo de un Long: " + Long.MIN_VALUE);
			System.out.println("Tamaño de un long: " + Long.SIZE);

		// Wrapper para el tipo de datos "short"
			System.out.println("\nValor maximo de un Short: " + Short.MAX_VALUE);
			System.out.println("Valor minimo de un Short: " + Short.MIN_VALUE);
			System.out.println("Tamaño de un short: " + Short.SIZE);

		// Wrapper para el tipo de datos "float"
		System.out.println("\nValor maximo de un Float: " + Float.MAX_VALUE);
		System.out.println("Valor minimo de un Float: " + Float.MIN_VALUE);
		System.out.println("Tamaño de un float: " + Float.SIZE);

		// Wrapper para el tipo de datos "double"
		System.out.println("\nValor maximo de un Double: " + Double.MAX_VALUE);
		System.out.println("Valor minimo para un Double: " + Double.MIN_VALUE);
		System.out.println("Tamaño de un double: " + Double.SIZE);

	}

}
